# There are two main types of loops: ‘for’ and ‘while’ loops. 
# A for loop is used to iterate over a sequence (that can be a list, tuple, dictionary, 
# string, or other iterable objects) and execute a block of code for each element in the sequence.
# A while loop is used to repeatedly execute a block of code as long as a condition is True. 
# It continues iterating until the condition becomes False.


# FOR loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
   print(fruit)

# WHILE loop
count = 1
while count <= 5:
    print(count)
    count +=1 
