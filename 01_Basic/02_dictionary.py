# A dictionary stores data values in key-values pairs. Dictionaries can be an array.

# Creating a dictionary of car brands and their corresponding models
car_models = {
    'Toyota': ['Corolla', 'Camry', 'Rav4'],
    'Honda': ['Civic', 'Accord', 'CR-V'],
    'Ford': ['Fusion', 'Escape', 'Mustang']
}

# Accessing values in the dictionary
print("Models by Toyota:", car_models['Toyota'])
print("Models by Ford:", car_models['Ford'])